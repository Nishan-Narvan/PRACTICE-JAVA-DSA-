import java.util.*;
public class Printnaturalnumbers {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter value of n ");
        int a = sc.nextInt();
        int i=a;
        while (i>=1){
            System.out.println(i);
            i=i-1;
        }


    }
}
